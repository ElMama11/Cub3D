#include "libft.h"

int	ft_str_is_alpha(char *src)
{
	int	i;

	i = 0;
	while (src && src[i])
	{
		if (! ((src[i] >= 65 && src[i] <= 90)
				|| (src[i] >= 97 && src[i] <= 122)))
			return (0);
		i++;
	}
	return (1);
}